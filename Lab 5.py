def main():
    print("This program turns numerical grade into letter grade")
    n=int(input("Enter numerical grade:"))
    if n >90: 
        print('A')
    elif n > 80: 
        print ('B')
    elif n >70: 
        print ('C')
    elif n >60: 
        print ('D')
    elif n >0: 
        print ('F')

main()
    
